#!/usr/bin/perl -w

# Float.pl

$x = 0.000000000003;                                               #(A)
$y = 1_234.000_000_000_003;                                        #(B)
print $x * $y, "\n";                # 3.70200000000001e-09         #(C)
printf "%.3e\n", $x * $y;           # 3.702e-09                    #(D)

$y = 1234 + 3.00e-12;                                              #(E)
print $x * $y, "\n";                # 3.70200000000001e-09         #(F)

$z = 1e50;                                                         #(G)
print $x / $z, "\n";                # 3e-62                        #(H)
